public class Calculation {
     boolean isEven (int number){
         return (number%2==0);
     }

     boolean isOdd (int number){
         return (number%2!=0);
     }

     double circleField (double number){
         return (number*number*Math.PI);
     }

     int power (int number){
         return (int)Math.pow(number,2);
     }
}
